// false: Kiểu dữ liệu Boolean với giá trị false.
// 0: Số nguyên 0.
// -0: Số nguyên 0 với dấu âm.
// 0n: BigInt với giá trị 0.
// "": Chuỗi rỗng.
// null: Giá trị đặc biệt chỉ đến sự không tồn tại của giá trị hoặc tham chiếu.
// undefined: Giá trị chỉ đến sự không xác định của biến.
// NaN: Not-a-Number, là một giá trị đặc biệt đại diện cho kết quả không phải là số.